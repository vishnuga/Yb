                                    <p><b>Functionalities</b></p>
                                    <p><i class="fa fa-bullseye"></i> Get innovative project ideas.</p>
                                    <p><i class="fa fa-bullseye"></i> Get suitable place for internships.</p>
                                    <p><i class="fa fa-bullseye"></i> Remote supervision. </p>
                                    <p><i class="fa fa-bullseye"></i> Keep track of the project progress.</p>
                                    <p><i class="fa fa-bullseye"></i> Produce project deliverables in an effective way.</p>
                                    <p><i class="fa fa-bullseye"></i> Manage, plan and schedule project works.</p>
