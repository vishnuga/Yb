                    <h3> <a href="#">Inroduction</a></h3>
                        
                        

                        <p>In Sri Lanka there are around 30 higher educational sectors available for students.
                                        Most of these sectors require the students to carry out their research oriented projects and/or
                                        industrial based internships in their final year of studies. At that time students, lecturers and
                                        industries face following problems. </p>
                                        <hr>

                            <b>Students</b>
                            <p><i class="fa fa-bullseye"></i> Choose an interest research topic </p>
                            <p><i class="fa fa-bullseye"></i> Select suitable place for internship  </p>
                            <p><i class="fa fa-bullseye"></i> Identify potential supervisors both in the academia and industry </p><br/>


                            <b>Lecturers</b>
                            <p><i class="fa fa-bullseye"></i> Access individual student contribution in a group project </p>
                            <p><i class="fa fa-bullseye"></i> Judge the requirements of stakeholders to undertake industry based research projects. </p><br/>

                            <b>Industries</b>
                            <p><i class="fa fa-bullseye"></i> Identify potential students with the required knowledge skills</p>
                            <p><i class="fa fa-bullseye"></i> Pay higher salary to employ a skillful software professional.</p>
                            <p><i class="fa fa-bullseye"></i> More time and money to promote themselves between university students. </p>


                            <hr>
                            <p>So we need a three way bridge system to solve these problems. </p>

