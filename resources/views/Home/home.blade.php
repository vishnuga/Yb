							<h3> <a href="ob">Objectives </a></h3>
							<p>Y-Bridge is a web-based matching module. It has the potential to bridge the gap between
                                        the university and industry by increasing relevance of thesis topics and by making the
                                        supervision process transparent and collaborative with academic and industries supervisors
                                        working in teams. </p>
